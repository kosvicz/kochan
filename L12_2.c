#include <stdio.h>

int main(void) {
    unsigned int w1 = 0525u, w2 = 0707u, w3 = 0122u;
    /*
     *  0 = 000     4 = 100
     *  1 = 001     5 = 101
     *  2 = 010     6 = 110
     *  3 = 011     7 = 111
     */

    printf("w1 & w2, %o %o %o\n", w1 & w2, w1 | w2, w1 ^ w2);
    /*
     *  w1 & w2:
     *  w1: 101 010 101
     *  w2: 111 000 111
     *  ---------------
     *      101 000 101 == 505
     *
     *  w1 | w2:
     *  w1: 101 010 101
     *  w2: 111 000 111
     *  ---------------
     *      111 010 111 == 727
     *
     *  w1 ^ w2:
     *  w1: 101 010 101
     *  w2: 111 000 111
     *  ---------------
     *      010 010 010 == 222
     */

    printf("%o %o %o\n", ~w1, ~w2, ~w3);
    /*
     *  ~w1:
     *  w1: 101 010 101
     *  ---------------
     *      010 101 010 == 252
     *
     *  ~w2:
     *  w2: 111 000 111
     *  ---------------
     *      000 111 000 == 070
     *
     *  ~w3:
     *  w3: 001 010 010
     *  ---------------
     *      110 101 101 == 655
     */

    printf("%o %o %o\n", w1 ^ w1, w1 & ~w2, w1 | w2 | w3);
    /*
     *  w1 ^ w1:
     *  w1: 101 010 101
     *  w1: 101 010 101
     *  ---------------
     *      000 000 000 = 0
     *
     *  w1 & ~w2:
     *  ~w2:
     *  w2: 111 000 111
     *  ---------------
     *  wx: 000 111 000
     *
     *  w1 & wx
     *  w1: 101 010 101
     *  wx: 000 111 000
     *  ---------------
     *      000 010 000 = 20
     *
     *  w1 | w2 | w2:
     *  w1: 101 010 101
     *  w2: 111 000 111
     *  ---------------
     *  wx: 111 010 111
     *  w3: 001 010 010
     *  ---------------
     *      111 010 111 = 727
     */

    printf("%o %o\n", w1 | w2 & w3, w1 | w2 & ~w3);
    /*
     *  w1 | w2 & w3:
     *  w1: 101 010 111
     *  w2: 111 000 111
     *  w3: 001 010 010
     *  ---------------
     *  wx: 101 010 111
     */

    printf("%o %o\n", ~(~w1 & ~w2), ~(~w1 | ~w2));
    w1 ^= w2;
    w2 ^= w1;
    w1 ^= w2;
    printf("w1 = %o, w2 = %o\n", w1, w2);

    return 0;
}

